# todo arquivo .py dentro de um projeto/pasta para o Python é um modulo
# quando se importa esse modulo, o script dele é executado por completo, o que pode trazer alguns problemas ao programa
# por isso pode-se ultilizar o: if __name__ == "__main__":
# if __name__ == "__main__" testa se o arquivo de script Python está sendo executado como arquivo principal ou não.
# quando o script é executado dentro do seu arquivo de origem, ele recebe o nome __main__
# quando o script é executado dentro de um outro arquivo que ele foi importado ele recebe o seu nome(__name__)
# if __name__ == "__main__" só vai executar o codigo abaixo dele, se o script estiver sendo executado no seu arquivo de origem(__main__)

