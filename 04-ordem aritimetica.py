# primeiro = parenteses ()
# segundo = exponenciação/potencia **
# terceiro = multiplicação*    divisão/      divisão inteira//    divisão resto%   na ordem em que aparecem no linha de código
# quarto = soma+   subtração-

soma = 5+5-(2+1)**(4/2)
