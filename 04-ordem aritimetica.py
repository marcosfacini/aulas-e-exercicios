# divisão comum mostra o resultado total incluindo as casas decimais
divisão = 4 / 3
# divisão inteira mostra somente o resultado inteiro ocultando as casas decimais
divisaointeira = 4 // 3
# mostra o que sobra de uma divisão
divisaoresto = 4 % 2

print (divisão)
print (divisaointeira)
print (divisaoresto)
